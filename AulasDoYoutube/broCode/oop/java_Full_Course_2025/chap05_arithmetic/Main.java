package AulasDoYoutube.broCode.oop.java_Full_Course_2025.chap05_arithmetic;

public class Main {
    public static void main(String[] args) {
        int x = 10;
        int y = 2;
        int z;

        // Augmented Assignment Operators
        z = x + y; // Soma
        z = x - y; // Subtracção
        z = x * y; // Multiplicação
        z = x / y; // Divisão

        System.out.println(z);

        // Increment and Decrement Operators

        int x1 = 1;

        x1++;

        System.out.println(x1);

    }
}
