package AulasDoYoutube.freeCodeCamp.JavaProgrammingForBeginners_FullCourse;

public class DataTypes {
    public static void main(String[] args) {
        // integer types
        byte aSingleByte = 100;
        short aSmallNumber = 20000;
        int anInteger = 2147483647;
        long aLargeNumber = 9223372036854775807L;

        // decimal types
        double aDouble = 1.79769313;
        float aFloat = 3.4028F;

        // booleans
        boolean isWeekend = false;
        boolean isWorday = true;

        // characters
        char copyrightSymbol = '\u00A9';
        System.out.println("This is the copyright symbol: " + copyrightSymbol);
    }
}
