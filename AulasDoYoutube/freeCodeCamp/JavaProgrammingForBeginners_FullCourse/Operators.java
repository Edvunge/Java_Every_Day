package AulasDoYoutube.freeCodeCamp.JavaProgrammingForBeginners_FullCourse;

public class Operators {
    public static void main(String[] args) {
        int number1 = 12;
        int number2 = 6;

        // addition
        System.out.println(number1 + number2);

        // subtraction
        System.out.println(number1 - number2);

        // multiplication
        System.out.println(number1 * number2);

        // division
        System.out.println(number1 / number2);

        // remainder
        System.out.println(number1 % number2);
    }
}
