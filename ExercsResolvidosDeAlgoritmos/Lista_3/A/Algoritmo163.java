package ExercsResolvidosDeAlgoritmos.Lista_3.A;

public class Algoritmo163 {
    public static void main(String[] args) {
        int voltaLagoa, abdominais;

        for (voltaLagoa = 1; voltaLagoa <= 3; voltaLagoa++) {
            System.out.println("\n" + voltaLagoa + "a volta na lagoa");
            for (abdominais = 1; abdominais <= 5; abdominais++) {
                System.out.println("\n " + abdominais + "o abdominais");
            }
        }
        System.out.println("\n");
    }
}
