package ExercsResolvidosDeAlgoritmos.Lista_3.A;

public class Algoritmo169 {
    public static void main(String[] args) {
        int l, c, t;
        System.out.println("\nABAIXO DA DIAGONAL PRINCIPAL\n");

        for (l = 2; l <= 10; l++) {
            System.out.println("\n");
            for (c = 1; c < l; c++) {
                System.out.println(l + "-" + c + "\t");
            }
        }
        System.out.println("\n");
    }
}
