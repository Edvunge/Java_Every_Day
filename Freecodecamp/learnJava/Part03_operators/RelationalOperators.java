package Freecodecamp.learnJava.Part03_operators;

public class RelationalOperators {
    public static void main(String[] args) {
        double number1 = 10;
        double number2 = 5;

        System.out.println(number1  == number2); // false
        System.out.println(number1  != number2); // true
        System.out.println(number1  >  number2); // true
        System.out.println(number1  <  number2); // false
        System.out.println(number1  >= number2); // true
        System.out.println(number1  <= number2); // false
    }
}
