package Freecodecamp.theJavaHandbook.how2_toVariables;

public class MainVariables {
    public static void main(String[] args) {
        // <type> <name>
        int age = 27;

        // <name> = <value>
        age = 28;
        // int age = 20;

        // prints the age on the terminal
        System.out.println("I am " + age + " years old.");
    }
}
