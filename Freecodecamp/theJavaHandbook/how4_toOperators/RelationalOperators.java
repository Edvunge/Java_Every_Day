package Freecodecamp.theJavaHandbook.how4_toOperators;

public class RelationalOperators {
    public static void main(String[] args) {
        double number1 = 10;
        double number2 = 5;

        System.out.println(number1 == number2);
        System.out.println(number1 != number2);
        System.out.println(number1 > number2);
        System.out.println(number1 < number2);
        System.out.println(number1 >= number2);
        System.out.println(number1 <= number2);
    }
}
