package JavaCompletoPOOprojetos.ApostilaJavaPOO.book.ApostilaJAVA.cap_3;

public class While {
    public static void main(String[] args) {
        int idade = 5;

        while (idade < 18) {
            System.out.println(idade);
            idade = idade + 1;
        }


        int i = 0;
        while (i < 10) {
            System.out.println(i);
            i = i + 1;
        }
    }
}
