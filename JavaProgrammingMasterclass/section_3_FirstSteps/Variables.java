package JavaProgrammingMasterclass.section_3_FirstSteps;

public class Variables {
    public static void main(String[] args) {
        // Data types
        char letter = 'a';
        int age = 23;
        float height = 1.98f;
        double weight = 1.23;

        System.out.format("letter: %c\n",letter);
        System.out.format("age: %d\n",age);
        System.out.format("height: %.2g\n",height);
        System.out.format("weight: %.2g\n",weight);
    }
}
