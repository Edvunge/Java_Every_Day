package UdemyCourses.javaComOOP_MatheusBattisti.section02_variables;

public class Exercicio01 {
    public static void main(String[] args) {
        /*
            EXERCICIO 1:

            Crie uma variavel int para armazenar o valor 10.
            Crie uma segunda int que armazena o dobro do valor da primeira variavel
            usando operadores aritmeticos.
            Exiba o resultado.
        */
        int value = 10;
        int dobro = (value * value);

        System.out.println("O primeiro valor: " + value);
        System.out.println("O dobro do valor: " + dobro);
    }
}
