package ZEDlistaDEexercicios.Lista_3_ComandosRepetetivos.Anexo1a20;

public class Exerc03 {
    // 3. Faca um algoritmo utilizando o comando while que mostra uma contagem regressiva
    // na tela, iniciando em 10 e terminando em 0.
    // Mostrar uma mensagem “FIM!” apos a contagem.
    public static void main(String[] args) {
        int i = 10;
        while (i > 0){
            System.out.println(i);
            i--;
        }
    }
}
