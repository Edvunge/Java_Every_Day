package geeksForGeeks.JavaCourse.Parte_6_Array.Arrays;

public class ArrayTraversal {
    public static void main(String[] args) {
        int arr[] = { 1, 2, 3, 4, 5 };
        // Traversing Over Arr[]
        for (int i = 0; i < arr.length; i++) {
            System.out.println(arr[i] + " ");
        }
    }
}
